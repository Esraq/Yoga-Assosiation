                <p>
                ইয়োগা বা যোগব্যায়াম একটি শাস্ত্রীয় কৌশল। যা পাঁচ হাজার বছরেরও পুরোনো। প্রাচীন ভারতীয় উপমহাদেশের মুনি ঋষিরা তাদের স্বাস্থ্য ঠিক রাখা এবং দীর্ঘজীবনের জন্য বিভিন্ন কলা-কৌশল আবিষ্কার বা আয়ত্ত করেন। প্রায় ৪০০ বছর আগে সর্বপ্রথম ঋষি পতঞ্জলি কিছু আসনের কথা বলেন এবং এগুলো মানুষের মাঝে ছড়িয়ে দেওয়ার চেষ্টা করেন। পরে ধীরে ধীরে এই কলাকৌশল ছড়িয়ে পড়ে পৃথিবীর সর্বত্র। উনবিংশ ও বিংশ শতাব্দীর দিকে বিভিন্ন দেশে বিভিন্ন ভাষায় ‘পতঞ্জলিআসনা’ নামে গ্রন্থটি ছড়িয়ে পড়ে। পরবর্তী সময়ে আরো অনেকেই যোগব্যায়ামের ওপর বেশকিছু গ্রন্থ রচনা করেন।

                    ‘ইয়োগা’ শব্দের আক্ষরিক অর্থ হচ্ছে ‘যুবক’ বা ‘যৌবন’। অর্থাৎ মানুষের দেহ ও মনের যৌবন ধরে রাখার কৌশল। এটা নিয়ে অনেকে অনেক রকম মতামত প্রকাশ করেছেন। কেউ বলেছেন আত্মা বা মন ও শরীরকে একত্র করার কৌশলকে ইয়োগা বা যোগ বলে।

                    ইয়োগা বা যোগব্যায়াম সাধারণত তিনটি প্রধান কাঠামোর ওপর নির্মিত হয়। যেমন ব্যায়াম, শ্বাস এবং ধ্যান। ব্যায়াম ও বিভিন্ন আসনের মাধ্যমে শরীরকে নিজের আয়ত্তে আনার কৌশল জানা যায় এবং বিভিন্ন রোগ থেকে নিজেকে মুক্ত রাখা যায়।</p>

