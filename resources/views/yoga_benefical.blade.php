                <p>মানসিক চাপ কমানোর শেষ চিকিৎসা : মানসিক চাপ দূরীকরণের শেষ ওষুধ হতে পারে ইয়োগা। গবেষণায় বলা হয়, ইয়োগা চর্চা স্পষ্টভাবে স্ট্রেস এবং রক্তচাপ নিয়ন্ত্রণ করে। এমনকি অ্যাংজাইটির মতো মারাত্মক মানসিক সমস্যার ঝুঁকি কমিয়ে আনে ইয়োগা।</p>
                <p>ঘাম ঝরানোর কাজে : এমনিতেও ইয়োগা দারুণ দৈহিক ব্যায়াম। হৃদযন্ত্রের সুস্থতা আনে এই ব্যায়াম। হৃদযন্ত্রকে আরো সুষ্ঠুভাবে রক্ত সঞ্চালনে সক্ষম করতে ইয়োগার বিকল্প নেই। গবেষণায় বলা হয়, বিশেষ করে মধ্যবয়সীদের জন্য ওজন নিয়্ন্ত্রণে সবচেয়ে স্বাস্থ্যকর উপায় হতে পারে ইয়োগা।</p>
                <p>দেহে শক্তি আনতে : ব্যায়ামের মাধ্যমে সুস্থতাসহ দেহ পায় শক্তি। পেশির আদর্শ গঠনে ইয়োগা দারুণ কার্যকর ব্যায়াম। হাত-পা এবং গোটা দেহের অতি জরুরি পেশিগুলোকে সুঠাম করতে সহায়ক এটি।</p>
                <p> স্মৃতিশক্তি বৃদ্ধিতে : স্মৃতিশক্তিকে ঝকঝকে করতে ইয়োগা উপকারী ব্যায়াম। 'সান সল্যুশনস' এবং 'ডাউনওয়ার্ড ডগ' চর্চা করে দেখুন। এতে মস্তিষ্কে কার্যকারিতার উন্নতি ঘটে। কঠিন ব্যায়ামের পরও এই ইয়োগা আপনার মস্তিষ্ককে পরবর্তী কাজে উৎসাহী করে তুলতে পারে।</p>
                <p> ব্যথা কমাতে : ঘাড়, হাঁটু বা পিঠের ব্যথা নিরসনে ওষুধের মতো কাজ করে ইয়োগা। দেহের এসব অংশের অ্যান্টিডোটের মতো কার্যকর ইয়োগা। হার্ভার্ড মেডিক্যাল স্কুলের গবেষণায় বলা হয়েছে, ক্রনিক ব্যথা দূর করতেও চিকিৎসাব্যবস্থার অন্যতম অংশ হতে পারে ইয়োগা।</p>
                <p>ঘুমের সমস্যা দূল করতে : যাদের ঘুম আসে না তাদের সেই সমস্যার সমাধান দেয় ইয়োগা। এর চর্চায় ইনসমনিয়ার মতো সমস্যা দূর হতে বেশি সময় লাগে না। রাতের বিশেষ কিছু ইয়োগা চর্চায় ঘুম হবে আরো গভীর ও স্বাস্থ্যকর।</p>
                <p>মাইগ্রেনের চিকিৎসায় : যাদের মাইগ্রেনের ব্যথা রয়েছে তাদের জন্যও চিকিৎসা হতে পারে ইয়োগা। ২০০৭ সালের এক গবেষণায় বলা হয়েছে, মাইগ্রেনের রোগীদের থেরাপির মতো কাজ করে ইয়োগা।</p>
                <p>মন ভালো করতে : মন-মেজাজ খারাপ থাকলে নিমিষেই তা ভালো করে দিতে পারে ইয়োগা। গবেষণায় বলা হয়, ইয়োগার মাধ্যমে যৌন তৃ্প্তি মেলে। নারী-পুরুষ উভয়ই যৌনজীবনটাকে তৃপ্তিকর করতে ইয়োগার আশ্রয় নিতে পারেন।</p>
                <p>চটপটে হতে : নিয়মিত ইয়োগার মাধ্যমে পেশি অনেক চটপটে কাজে সহনীয় হয়ে ওঠে। আপনি আগের চেয়ে অনেক বেশি উচ্ছ্ল ও উদ্যমী হয়ে উঠবেন ইয়োগার কল্যাণে।</p>
                <p> সুখী হতে : সুখকর অনুভূতি দেয় ইয়োগা। দুঃখভারাক্রান্ত হৃদয় নিয়ে ইয়োগায় বসলে মুহূর্তেই মনটা ভালো হয়ে যায়। দীর্ঘমেয়াদি সুখ লাভে তাই ইয়োগা অতুলনীয় এক ব্যায়াম।</p>
